// Do..While loop: execute statement at least once, before checking the condition

public class Example3 { //main class starts
    public static void main(String[] args) { // main method starts
        // starts your code here
        int x = 1;
        do {
            System.out.println("I am executed, yay!");
            x++;
        } while (x< 10);
    } // end of main method
} // main class end
